"""
Numbers examples
"""
# There are 3 number datatypes in Python
x = 1   # int
x = 3.5 # float
x = 1j  # complex

# Int
x = 213612783613682163621638268362176 # The int is unlimited
print(x) # 213612783613682163621638268362176

# Float
x = 3.9826321638217637216371283686213 # The float is limited
print(x) # 3.982632163821764
x = 3.5e100 # Float can be scientific numbers with an "e" to indicate the power of 10
print(x) # 3.5e+100

# Complex
x = 1+4j # 'j' indicates the complex (imaginary) number
print(x) # (1+4j)
print(4j * 4j) # (-16+0j) (Complex multiplication)