"""
Numbers

Number types of Python:
* int
* float
* complex
"""


# There are 3 number datatypes in Python
x = 1    # int
x = 3.5  # float
x = 1j   # complex


# Int (unlimited)
x = 213612783613682163621638268362176
print(x)
# 213612783613682163621638268362176


# Float (limited)
x = 3.9826321638217637216371283686213
print(x)
# 3.982632163821764


# Float as scientific numbers
# "e" to indicate the power of 10
x = 3.5e100
print(x)
# 3.5e+100


# Complex
# 'j' indicates the complex (imaginary) number
x = 1+4j
print(x)
# (1+4j)
print(4j * 4j)
# (-16+0j) (Complex multiplication)
