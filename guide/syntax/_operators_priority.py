"""
Operators priority

-------------------------------------------------------------------------------
Priority	    Operator	    kind        Example
-------------------------------------------------------------------------------
1	            +, -	        unary       -2, +1
2	            **                          3 ** 2
3	            *, /, //, %                 4 / 2, 5 * 2, 6 % 5
4	            +, -	        binary      4 + 4, 7 - 3
5	            <, <=, >, >=	            5 > 3, 4 <= 4
6	            ==, !=                      3 == 3, 2 != 5
-------------------------------------------------------------------------------
"""


# Expression 1
x = 3 + 5 * 2
print(x)
# 13


# Expression 2
x = 2 ** 2 / 4 * 1
print(x)
# 1.0


# Expression 3
x = 1 - 3 * -4 // -5.
print(x)
# -1.0


# Right sided binding (**)
# NOTE: Important!
print(2 ** 2 ** 3)
# It is calculed as 2 ** (2 ** 4)
# 256
