"""
Bitwise Sum
"""


# Input
x = 28714  # 0111 0000 0010 1010
y = 23784  # 0101 1100 1110 1000


# Algorithm
while y != 0:
    result = x ^ y
    carry = (x & y) << 1
    x = result
    y = carry
print(result)

